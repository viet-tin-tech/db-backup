#!/usr/bin/env bash

export PGPASSWORD=${PGPASSWORD:-111222}

RESTORE_DATABASE=$1
DUMP_FILE=$2
DB_HOST=${DB_HOST:-10.130.0.104}
DB_USER=${DB_USER:-admin}

psql -U $DB_USER -h $DB_HOST postgres -c "CREATE DATABASE IF NOT EXISTS $RESTORE_DATABASE;"
gunzip -c $DUMP_FILE | psql -U $DB_USER -h $DB_HOST $RESTORE_DATABASE